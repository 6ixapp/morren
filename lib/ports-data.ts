// Helper function to extract city/state from address
export function extractLocationFromAddress(address: string): { city?: string; state?: string; pincode?: string } {
    if (!address) return {};

    const parts = address.split(',').map(s => s.trim());
    const pincodeMatch = address.match(/\b\d{6}\b/);

    return {
        city: parts.length > 1 ? parts[parts.length - 2] : undefined,
        state: parts.length > 0 ? parts[parts.length - 1] : undefined,
        pincode: pincodeMatch ? pincodeMatch[0] : undefined,
    };
}

// Haversine formula to calculate distance between two coordinates
export function calculateDistance(lat1: number, lon1: number, lat2: number, lon2: number): number {
    const R = 6371; // Earth's radius in km
    const dLat = (lat2 - lat1) * Math.PI / 180;
    const dLon = (lon2 - lon1) * Math.PI / 180;
    const a =
        Math.sin(dLat / 2) * Math.sin(dLat / 2) +
        Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
        Math.sin(dLon / 2) * Math.sin(dLon / 2);
    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
    return R * c;
}

// Function to find nearest port from address
export function findNearestPort(address: string): { port: typeof INDIAN_PORTS[0]; distance: number } | null {
    if (!address) return null;

    const location = extractLocationFromAddress(address);
    let coordinates: { lat: number; lon: number } | null = null;

    // Try to find coordinates based on city name
    if (location.city) {
        const cityKey = Object.keys(CITY_COORDINATES).find(
            city => city.toLowerCase() === location.city?.toLowerCase()
        );
        if (cityKey) {
            coordinates = CITY_COORDINATES[cityKey];
        }
    }

    // If no city match, try state-based approximation
    if (!coordinates && location.state) {
        const stateKey = Object.keys(CITY_COORDINATES).find(
            city => city.toLowerCase() === location.state?.toLowerCase()
        );
        if (stateKey) {
            coordinates = CITY_COORDINATES[stateKey];
        }
    }

    // If still no coordinates, return null
    if (!coordinates) return null;

    // Calculate distances to all ports
    const portsWithDistances = INDIAN_PORTS.map(port => ({
        port,
        distance: calculateDistance(coordinates!.lat, coordinates!.lon, port.lat, port.lon)
    }));

    // Sort by distance and return nearest
    portsWithDistances.sort((a, b) => a.distance - b.distance);
    return portsWithDistances[0];
}
